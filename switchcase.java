public class switchcase {
    
    public static void main(String[] args){
        
        String nVal ="A";
        
        switch (nVal) {
            case "A":
                System.out.println("Nilai nVal adalah A");
                break;
            case "B":
                System.out.println("Nilai nVal adalah B");
                break;
            case "C":
                System.out.println("Nilai nVal adalah C");
                break;
            case "D":
                System.out.println("Nilai nVal adalah D");
                break;
            default:
                System.out.println("Nilai nVal tidak ada dalam default");
            
        }
    }
}
